<span class="menu_smaller">Introduction</span>
<p>
    Hi everyone! Your friendly neighborhood Squega here. Welcome to The Necro Lab! I am super proud to present this new and improved version of the site as it is a culmination of a lot of development work and learning over the course of a year and a half since its original launch. I hope you enjoy it!
</p>
<p>
    Here you will find leaderboard rankings from Crypt of the NecroDancer's Steam version. You can also find power and daily rankings derived from the leaderboards. This data goes as far back as just before the game's original release on April 23rd, 2015. Most of it is already available to view via the top menu of every page.
</p>
<p>
    You can also login into Necro Lab with your Steam credentials! Just find your Steam username, click on it, go to your Necro Lab profile, and click on the green login button at the top to log in using Steam. It's all secure, and credentials are handled entirely on Steam's side via OpenID.
</p>
<p>
    What can you do after you log in? You can link other accounts to the site! The current sites you can link to are: Beam.pro, Discord, Reddit, Twitch, Twitter, and Youtube. Once you're logged in your player profile page will have buttons to connect your account to each of those sites. Connecting these accounts to your profile will allow you to filter rankings by that site and see how they stack up against you and the others! This process is also secure, and entirely handled by their respective sites via OAuth.
</p>
<p>
    There's also an API that the site runs off of and is available to anyone to take advantage of. Please head over to <a class="underline" href="/about/api">the documentation</a> for this API to get started!
</p>
<span class="menu_smaller">History</span>
<p>
    The very beginnings of this site started as a <a class="underline" href="https://docs.google.com/spreadsheets/d/1F_cTvQp8gehUnRpdZ2fKqHhE7Gobrcf2tXLPkvEpGyg/edit?pli=1#gid=391506336" target="_blank">Google Document</a> created by <a class="underline" href="http://www.twitch.tv/wilarseny" target="_blank">Wilarseny</a> during the game's pre-release beta. Wilarseny would retrieve data from the leaderboards, scrub the data, and calculate power rankings for the top 100 players using a series of spreadsheet formulas as the algorithm. This was manually intensive task so he collaborated with myself to build a website to automate the calculation of these power rankings. 
</p>
<p>
    During this site's development the algorithm originally used in the spreadsheets, intended to calculate only the top 100 players, was changed by <a class="underline" href="http://www.twitch.tv/jakkdl" target="_blank">Jakkdl</a> to apply to all players in the leaderboards. This algorithm was expanded to also apply to the dailies as well.
</p>
<p>
    The site launched shortly prior to the game's launch and has been collecting data since.
</p>
<p>
    After initial features and bug fixes were made to the site my work on its development slowed down significantly as my life got really busy for the next year. However, at the end of 2016, and when we learned that the DLC EA was being released, I started work on rewriting the site to address many of its issues and handle the data from the DLC.
</p>
<p>
    Over the course of several months of intense development here we are! I hope this site provides as much utility to the NecroDancer community as is possible.
</p>
